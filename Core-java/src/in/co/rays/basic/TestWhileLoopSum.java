package in.co.rays.basic;



public class TestWhileLoopSum {

	
	
public static void main(String[] args) {
	
	
	
	int i = 1;
	
	
    int  sum = 0;
    
    
    while (i<=20) {
    
    	
    	
    	System.out.println(i);
    	
    	
    	
    	sum = sum + i;
    	
    	
    	
    	 i++;
    }
    
    
    System.out.println("sum =" +sum);
    
    
    }


}
