package in.co.rays.slides;

public class MathFunctions {
	
	public static void main(String[] args) {
		
		System.out.println("Mathematic function");
		System.out.println("Max " + Math.max(2, 5));
		System.out.println("min " + Math.min(2, 5));
		System.out.println("absolute  " + Math.abs(-3.7)); // absolute converts any negative value(-2) to positive value (2);
		System.out.println("exp  " + Math.exp(10));
		System.out.println("random number " + Math.random());
		System.out.println("square root " + Math.sqrt(25));
		
	}

}
